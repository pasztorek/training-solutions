package debug;

public class Employee {

        private int yerOfBirth;
        private String name;


    public Employee(int yerOfBirth, String name) {
        this.yerOfBirth = yerOfBirth;
        this.name = name;
    }


    public int getYerOfBirth() {
        return yerOfBirth;
    }

    public String getName() {
        return name;
    }
}
